import React, { useState, useEffect } from "react";
import { useValidation } from "@/hooks/useValidation";
import Input from "@/components/ui/Input";
import Button from "@/components/ui/Button";
import { useAuth } from "@/hooks/useAuth";
import { useZxing } from "react-zxing";

const ValidationPortal: React.FC = () => {
  const [codeInput, setCodeInput] = useState("");
  const [showQRScanner, setShowQRScanner] = useState(false);
  const [isCameraReady, setIsCameraReady] = useState(false);
  const [cameraError, setCameraError] = useState<string | null>(null);
  const [stream, setStream] = useState<MediaStream | null>(null);

  const { state, result, errorMessage, validate, validateQR, history, reset } = useValidation();
  const { logout, user } = useAuth();

  const officerName = user
    ? `${user.firstName ?? ""} ${user.lastName ? user.lastName.charAt(0) + "." : ""}`
    : "Guest";

  // üîπ Manual validation
  const handleValidationSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    if (codeInput.length === 5 && state !== "loading") {
      validate(codeInput);
    }
  };

  // üîπ QR validation
  const handleQRScan = async (data: string) => {
    if (data) {
      stopCamera();
      setShowQRScanner(false);
      await validateQR(data);
    }
  };

  // üîπ Setup camera with zxing
  const { ref: videoRef } = useZxing({
    onDecodeResult(result) {
      if (result) handleQRScan(result.getText());
    },
    paused: !showQRScanner,
  });

  // üîπ Start camera
  const startCamera = async () => {
    try {
      setCameraError(null);
      const mediaStream = await navigator.mediaDevices.getUserMedia({
        video: { facingMode: "environment" },
      });
      const video = videoRef.current as HTMLVideoElement | null;
      if (video) {
        video.srcObject = mediaStream;
        video.play();
      }
      setStream(mediaStream);
      setIsCameraReady(true);
    } catch (err) {
      console.error("Camera access error:", err);
      setCameraError("Unable to access camera. Please check permissions.");
    }
  };

  // üîπ Stop camera
  const stopCamera = () => {
    if (stream) {
      stream.getTracks().forEach((track) => track.stop());
      setStream(null);
      setIsCameraReady(false);
    }
  };

  useEffect(() => {
    if (showQRScanner) startCamera();
    else stopCamera();
    return () => stopCamera();
  }, [showQRScanner]);

  // üîπ Close modal manually
  const closeModal = () => {
    reset();
    setCodeInput("");
  };

  // üîπ Keep modal stable: don‚Äôt reset automatically while validating
  useEffect(() => {
    // We removed the "reset when typing" here ‚Äî it caused flicker issues
  }, []);

  // üîπ Render Modal
  const renderModal = () => {
    if (state === "success" && result) {
      return (
        <div style={overlayStyle}>
          <div style={{ ...cardStyle, borderColor: "#28a745", background: "#eaffea" }}>
            <h2 style={{ color: "#155724" }}>ACCESS GRANTED ‚úÖ</h2>
            <p>Generated by: <strong>{result.residentName}</strong></p>
            <p>
              Home:{" "}
              <strong>
                {`${result.homeDetails?.plotNumber ?? ""} ${result.homeDetails?.street ?? ""}`}
              </strong>
            </p>
            <p>Visitor: <strong>{result.visitorName}</strong></p>
            <Button onClick={closeModal}>OK</Button>
          </div>
        </div>
      );
    }

    if (state === "denied" || state === "error") {
      return (
        <div style={overlayStyle}>
          <div style={{ ...cardStyle, borderColor: "#dc3545", background: "#ffeaea" }}>
            <h2 style={{ color: "#a00000" }}>ACCESS DENIED ‚ùå</h2>
            <p>{errorMessage || "Invalid access code"}</p>
            <Button onClick={closeModal} variant="danger">OK</Button>
          </div>
        </div>
      );
    }

    return null;
  };

  return (
    <div style={pageStyle}>
      {/* HEADER */}
      <header style={headerStyle}>
        <div>
          <h1 style={{ fontSize: "20px" }}>Security Validation Portal</h1>
          <p style={{ fontSize: "12px" }}>Officer: <strong>{officerName}</strong></p>
        </div>
        <div style={{ textAlign: "right" }}>
          <p style={{ fontSize: "12px", marginBottom: "4px" }}>
            {new Date().toLocaleDateString("en-US", { weekday: "short", month: "short", day: "numeric" })}{" "}
            {new Date().toLocaleTimeString([], { hour: "2-digit", minute: "2-digit" })}
          </p>
          <button onClick={logout} style={logoutBtnStyle}>LOGOUT</button>
        </div>
      </header>

      {/* FORM */}
      <main style={formContainerStyle}>
        <h3>Validate Access Code</h3>
        <form onSubmit={handleValidationSubmit} style={formStyle}>
          <Input
            label=""
            type="text"
            maxLength={5}
            value={codeInput.toUpperCase()}
            onChange={(e) => setCodeInput(e.target.value.toUpperCase())}
            placeholder="Enter Access Code"
            style={inputStyle}
          />
          <Button type="submit" disabled={state === "loading"} style={buttonStyle}>
            {state === "loading" ? "Validating..." : "VALIDATE"}
          </Button>
        </form>

        <Button onClick={() => setShowQRScanner(!showQRScanner)} style={{ marginTop: "10px" }}>
          {showQRScanner ? "Close Scanner" : "Scan QR Code"}
        </Button>
      </main>

      {/* QR SCANNER */}
      {showQRScanner && (
        <div style={scannerContainerStyle}>
          <video ref={videoRef} autoPlay playsInline style={videoStyle} />
          {!isCameraReady && !cameraError && <p style={infoText}>Activating camera...</p>}
          {cameraError && <p style={errorText}>{cameraError}</p>}
          {isCameraReady && !cameraError && (
            <p style={infoText}>Scanning... Hold your QR code steady.</p>
          )}
        </div>
      )}

      {/* RECENT VALIDATIONS */}
      <section style={tableSectionStyle}>
        <h3>Recent Validations</h3>
        <div style={{ overflowX: "auto" }}>
          <table style={tableStyle}>
            <thead>
              <tr>
                <th>Time</th>
                <th>Code</th>
                <th>Visitor</th>
                <th>Resident</th>
                <th>Status</th>
              </tr>
            </thead>
            <tbody>
              {history.length > 0 ? (
                history.slice(0, 5).map((log) => (
                  <tr key={log.id}>
                    <td>{new Date(log.timestamp).toLocaleTimeString([], { hour: "2-digit", minute: "2-digit" })}</td>
                    <td>{log.code}</td>
                    <td>{log.visitorName}</td>
                    <td>{log.residentName || "N/A"}</td>
                    <td
                      style={{
                        color: log.status.toLowerCase() === "granted" ? "#28a745" : "#dc3545",
                        fontWeight: 600,
                        textTransform: "uppercase",
                      }}
                    >
                      {log.status.toUpperCase()}
                    </td>
                  </tr>
                ))
              ) : (
                <tr>
                  <td colSpan={5} style={{ textAlign: "center", padding: "15px", color: "#777" }}>
                    No recent validations
                  </td>
                </tr>
              )}
            </tbody>
          </table>
        </div>
      </section>

      {/* MODAL */}
      {renderModal()}
    </div>
  );
};

export default ValidationPortal;

